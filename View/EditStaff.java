    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:
        int staffid=Integer.parseInt((String) ids.getSelectedItem());
        DBAccess d=new DBAccess();
        String name1=name.getText();
        String email1=email.getText();
        String cn1=cn.getText();
        double salary1=Double.parseDouble(salary.getText());
        String shift1=shift.getText();
        String role1=(String) Roles.getSelectedItem();
        try {
            d.updatestaff(staffid, name1, cn1, email1, salary1, shift1, role1);
        } catch (SQLException ex) {
            Logger.getLogger(EditStaff.class.getName()).log(Level.SEVERE, null, ex);
        }
        name.setText("");
        cn.setText("");
        email.setText("");
        salary.setText("");
        shift.setText("");
        
    }//GEN-LAST:event_UpdateActionPerformed

    private void getinfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getinfoActionPerformed
        // TODO add your handling code here:
        int staffid=Integer.parseInt((String) ids.getSelectedItem());
        DBAccess d=new DBAccess();
        Staff s=null;
        
        try {
            s = d.getstaffinfo(staffid);
        } catch (SQLException ex) {
            Logger.getLogger(EditStaff.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(s!=null){
            name.setText(s.getName());
        cn.setText(s.getCn());
        email.setText(s.getEmail());
        String x=s.getSalary()+"";
        salary.setText(x);
        shift.setText(s.getShift());
        Roles.setSelectedItem(s.getrole());
        
        }
        
        
    }//GEN-LAST:event_getinfoActionPerformed

    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        // TODO add your handling code here:
        int staffid=Integer.parseInt((String) ids.getSelectedItem());
        DBAccess d=new DBAccess();
        d.deletestaff(staffid);
        name.setText("");
        cn.setText("");
        email.setText("");
        salary.setText("");
        shift.setText("");
        try {
            fillidcombo();
        } catch (Exception ex) {
            Logger.getLogger(EditStaff.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_removeActionPerformed

