        private void fillidcombo() throws Exception
       {
           ids.removeAllItems();
           DBAccess d= new DBAccess();
           ArrayList<Integer> idlist = new ArrayList();
           idlist=d.getstaffIds();
           for(int i=0; i<idlist.size();i++)
           {
               ids.addItem(""+idlist.get(i));
           }
       }
        
        private void fillrolecombo() throws Exception
       {
           Roles.removeAllItems();
           DBAccess d= new DBAccess();
           ArrayList<String> namelist = new ArrayList();
           namelist=d.getroles();
           for(int i=0; i<namelist.size();i++)
           {
               Roles.addItem(""+namelist.get(i));
           }
       }

